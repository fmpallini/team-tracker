const dicts: Record<Locale, Record<MsgKey, string>> = {
  'pt-BR': pt,
  'en-US': en,
}

export function t(locale: Locale, key: MsgKey, params?: Record<string, string>): string {
  let msg = dicts[locale][key]
  if (params) {
    for (const [name, value] of Object.entries(params)) {
      msg = msg.split(`{${name}}`).join(value)
    }
  }
  return msg
}

function pad2(n: number): string {
  return n < 10 ? `0${n}` : `${n}`
}

export function formatDate(iso: string, locale: Locale): string {
  const [y, m, d] = iso.split('-')
  if (locale === 'pt-BR') return `${d}/${m}/${y}`
  return `${m}/${d}/${y}`
}

export function parseLocaleDate(s: string, locale: Locale): string | null {
  const parts = s.split('/')
  if (parts.length !== 3) return null
  const [a, b, c] = parts as [string, string, string]
  const dayStr = locale === 'pt-BR' ? a : b
  const monthStr = locale === 'pt-BR' ? b : a
  const yearStr = c
  if (!/^\d{1,4}$/.test(dayStr) || !/^\d{1,2}$/.test(monthStr) || !/^\d{4}$/.test(yearStr)) return null
  const day = Number(dayStr)
  const month = Number(monthStr)
  const year = Number(yearStr)
  if (!Number.isInteger(day) || !Number.isInteger(month) || !Number.isInteger(year)) return null

  const date = new Date(year, month - 1, day)
  if (date.getFullYear() !== year || date.getMonth() !== month - 1 || date.getDate() !== day) {
    return null
  }
  return `${year}-${pad2(month)}-${pad2(day)}`
}

export function todayIso(): string {
  const now = new Date()
  return `${now.getFullYear()}-${pad2(now.getMonth() + 1)}-${pad2(now.getDate())}`
}
